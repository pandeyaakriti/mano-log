// Utility functions
export const getPersonalizedDailyAffirmation = (userName?: string): string => {
  const days = ['sunday', 'monday', 'tuesday', 'wednesday', 'thursday', 'friday', 'saturday'];
  const today = new Date();
  const dayName = days[today.getDay()] as keyof typeof DAY_SPECIFIC_AFFIRMATIONS;
  
  // Get different affirmation based on date to avoid weekly repetition
  const dayOfMonth = today.getDate();
  const dayAffirmations = DAY_SPECIFIC_AFFIRMATIONS[dayName];
  const affirmationIndex = dayOfMonth % dayAffirmations.length;
  
  let affirmation = dayAffirmations[affirmationIndex];
  
  // Personalize with user's name occasionally (every 3rd day)
  if (dayOfMonth % 3 === 0 && userName && userName !== 'friend') {
    affirmation = affirmation.replace(/You/g, `${userName}, you`);
  }
  
  return affirmation;
};

